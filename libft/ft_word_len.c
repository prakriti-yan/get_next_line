int	ft_word_len(const char *s, char c)
{
	int len;
	int i;

	i = 0;
	len = 0;
	while (s[i] == c && s[i])
		i++;
	while (s[i] && s[i] != c)
	{
		len++;
		i++;
	}
	return (len);
}
